export type ThemeCategory =
  | "vibrant"
  | "purple"
  | "blue"
  | "black"
  | "green"
  | "red"
  | "amber"
  | "teal"

export type Palette = {
  background: string
  foreground: string
  card: string
  "card-foreground": string
  popover: string
  "popover-foreground": string
  primary: string
  "primary-foreground": string
  secondary: string
  "secondary-foreground": string
  muted: string
  "muted-foreground": string
  accent: string
  "accent-foreground": string
  destructive: string
  "destructive-foreground": string
  success: string
  "success-foreground": string
  warning: string
  "warning-foreground": string
  border: string
  input: string
  ring: string
}

export type Theme = {
  id: string
  name: string
  category: ThemeCategory
  mode: "dark" | "light"
  swatchBg: string
  swatchPrimary: string
  swatchAccent: string
  palette: Palette
}

function hslToHex(h: number, s: number, l: number): string {
  const sPct = s / 100
  const lPct = l / 100
  const c = (1 - Math.abs(2 * lPct - 1)) * sPct
  const x = c * (1 - Math.abs(((h / 60) % 2) - 1))
  const m = lPct - c / 2
  let r = 0
  let g = 0
  let b = 0
  if (h < 60) [r, g, b] = [c, x, 0]
  else if (h < 120) [r, g, b] = [x, c, 0]
  else if (h < 180) [r, g, b] = [0, c, x]
  else if (h < 240) [r, g, b] = [0, x, c]
  else if (h < 300) [r, g, b] = [x, 0, c]
  else [r, g, b] = [c, 0, x]
  const toHex = (v: number) =>
    Math.round((v + m) * 255)
      .toString(16)
      .padStart(2, "0")
  return `#${toHex(r)}${toHex(g)}${toHex(b)}`
}

type DarkSpec = {
  id: string
  name: string
  category: ThemeCategory
  bgHue: number
  bgSat: number
  bgLight: number
  primaryHue: number
  primarySat: number
  primaryLight: number
}

function makeDark(s: DarkSpec): Theme {
  const { bgHue, bgSat, bgLight, primaryHue, primarySat, primaryLight } = s
  const surfaceLight = Math.min(bgLight + 4, 22)
  const elevatedLight = Math.min(bgLight + 8, 28)
  const accentLight = Math.min(bgLight + 14, 34)
  const borderLight = Math.min(bgLight + 10, 30)
  const fgSat = Math.min(bgSat + 15, 55)
  const fgLight = 92
  const mutedFgSat = Math.max(Math.floor(bgSat * 0.6), 10)
  const mutedFgLight = 65

  const hsl = (h: number, sat: number, light: number) => `${h} ${sat}% ${light}%`

  return {
    id: s.id,
    name: s.name,
    category: s.category,
    mode: "dark",
    swatchBg: hslToHex(bgHue, bgSat, bgLight),
    swatchPrimary: hslToHex(primaryHue, primarySat, primaryLight),
    swatchAccent: hslToHex(bgHue, bgSat, accentLight),
    palette: {
      background: hsl(bgHue, bgSat, bgLight),
      foreground: hsl(bgHue, fgSat, fgLight),
      card: hsl(bgHue, bgSat, surfaceLight),
      "card-foreground": hsl(bgHue, fgSat, fgLight),
      popover: hsl(bgHue, bgSat, surfaceLight),
      "popover-foreground": hsl(bgHue, fgSat, fgLight),
      primary: hsl(primaryHue, primarySat, primaryLight),
      "primary-foreground": primaryLight > 60 ? hsl(bgHue, bgSat, bgLight) : "0 0% 100%",
      secondary: hsl(bgHue, Math.floor(bgSat * 0.85), elevatedLight),
      "secondary-foreground": hsl(bgHue, fgSat, fgLight),
      muted: hsl(bgHue, Math.floor(bgSat * 0.85), elevatedLight),
      "muted-foreground": hsl(bgHue, mutedFgSat, mutedFgLight),
      accent: hsl(bgHue, bgSat, accentLight),
      "accent-foreground": hsl(bgHue, fgSat, fgLight),
      destructive: "0 75% 60%",
      "destructive-foreground": "0 0% 100%",
      success: "142 60% 50%",
      "success-foreground": "0 0% 100%",
      warning: "38 90% 58%",
      "warning-foreground": "0 0% 10%",
      border: hsl(bgHue, Math.floor(bgSat * 0.8), borderLight),
      input: hsl(bgHue, Math.floor(bgSat * 0.8), borderLight),
      ring: hsl(primaryHue, primarySat, primaryLight),
    },
  }
}

// Vibrant themes: deep, saturated backgrounds paired with a neon primary and a
// distinct second accent hue, so the swatch reads as bold/multi-colour. The
// `accent` UI surface stays subtle (it drives hover states) while the energy
// comes from the saturated background + neon primary.
type VibrantSpec = {
  id: string
  name: string
  bgHue: number
  bgSat: number
  bgLight: number
  primaryHue: number
  primarySat: number
  primaryLight: number
  accentHue: number
}

function makeVibrant(s: VibrantSpec): Theme {
  const { bgHue, bgSat, bgLight, primaryHue, primarySat, primaryLight, accentHue } = s
  const hsl = (h: number, sat: number, light: number) => `${h} ${sat}% ${light}%`
  const surface = Math.min(bgLight + 4, 24)
  const elevated = Math.min(bgLight + 9, 30)
  const accentSurface = Math.min(bgLight + 13, 34)
  const borderLight = Math.min(bgLight + 11, 32)

  return {
    id: s.id,
    name: s.name,
    category: "vibrant",
    mode: "dark",
    swatchBg: hslToHex(bgHue, bgSat, bgLight),
    swatchPrimary: hslToHex(primaryHue, primarySat, primaryLight),
    swatchAccent: hslToHex(accentHue, 88, 62),
    palette: {
      background: hsl(bgHue, bgSat, bgLight),
      foreground: hsl(bgHue, 25, 96),
      card: hsl(bgHue, Math.floor(bgSat * 0.9), surface),
      "card-foreground": hsl(bgHue, 25, 96),
      popover: hsl(bgHue, Math.floor(bgSat * 0.9), surface),
      "popover-foreground": hsl(bgHue, 25, 96),
      primary: hsl(primaryHue, primarySat, primaryLight),
      "primary-foreground":
        primaryLight > 58 ? hsl(bgHue, bgSat, Math.max(bgLight - 2, 6)) : "0 0% 100%",
      secondary: hsl(bgHue, Math.floor(bgSat * 0.7), elevated),
      "secondary-foreground": hsl(bgHue, 25, 96),
      muted: hsl(bgHue, Math.floor(bgSat * 0.7), elevated),
      "muted-foreground": hsl(bgHue, 18, 70),
      accent: hsl(accentHue, 32, accentSurface),
      "accent-foreground": hsl(bgHue, 25, 96),
      destructive: "0 82% 62%",
      "destructive-foreground": "0 0% 100%",
      success: "150 72% 50%",
      "success-foreground": "0 0% 100%",
      warning: "40 95% 58%",
      "warning-foreground": "0 0% 10%",
      border: hsl(bgHue, Math.floor(bgSat * 0.6), borderLight),
      input: hsl(bgHue, Math.floor(bgSat * 0.6), borderLight),
      ring: hsl(primaryHue, primarySat, primaryLight),
    },
  }
}

